let message = document.querySelector('.message');
let typingSpeed = 7;

function toggleText() {
    if (isTyping) return; // Не прерываем набор текста

    clearInterval(typingInterval);

    if (isOriginalText) {
        typeText(secretText);
    } else {
        copyToClipboard();
    }
}

function typeText(text) {
    let i = 0;
    message.textContent = '';
    message.classList.add('typing');
    isTyping = true;
    isOriginalText = false;

    typingInterval = setInterval(() => {
        message.textContent += text[i];
        i++;

        if (i === text.length) {
            clearInterval(typingInterval);
            message.classList.remove('typing');
            isTyping = false;
        }
    }, typingSpeed);
}

// Копирование текста и плавное появление "Copied!"
function copyToClipboard() {
    navigator.clipboard.writeText(secretText).then(() => {
        message.classList.add('fade-out');
        setTimeout(() => {
            message.textContent = "Copied!";
            message.classList.remove('fade-out');
            message.classList.add('fade-in');
            isOriginalText = false;
            message.onclick = fadeToOriginal;
        }, 2000); // Медленная смена через 2 секунды
    });
}

// Плавное возвращение оригинального текста
function fadeToOriginal() {
    message.classList.add('fade-out');
    setTimeout(() => {
        message.textContent = originalText;
        message.classList.remove('fade-out');
        message.classList.add('fade-in');
        isOriginalText = true;
        message.onclick = toggleText;
    }, 2000); // Плавная смена текста за 2 секунды
}
